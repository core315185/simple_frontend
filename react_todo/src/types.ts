// ================================================================
// types.ts — アプリ全体で使う型定義
// ================================================================
//
// 【TypeScriptの考え方①: interface でオブジェクトの形を定義する】
//   interface は「このオブジェクトはこういうプロパティを持つ」という
//   設計図です。型が合わないとコンパイルエラーになり、バグを早期発見できます。
//
//   type でも同じことができます：
//     type Todo = { id: number; text: string; completed: boolean };
//   シンプルな構造には type、拡張の可能性があるなら interface が慣習です。
//
// ================================================================

export interface Todo {
  id: number;
  text: string;
  completed: boolean;
}
